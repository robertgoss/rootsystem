testThesisUnit = testGroup "Unit tests" [testSpin16QuotientElementNumber,
										testDoubleQuoE8Union,
										testDoubleQuoE8Distinct,
										testSpin4Spin12QuotientElementNumber,
										testDoubleQuoS3E7Union,
										testDoubleQuoS3E7Distinct,
